import type { TemplateCategoryKey } from './travelTemplateCategories';

export interface LibraryTemplate {
  key: string;                    // stable unique id used for idempotent import
  category: TemplateCategoryKey;
  locale: 'ar' | 'en';
  name: string;                   // Meta-safe name (lower_snake)
  displayName: string;            // human title
  description: string;
  header?: string;
  body: string;
  footer?: string;
  variables: string[];            // list of {{var}} names used
  tags: string[];
  previewVariables?: Record<string, string>;
  suggestFor?: {                  // where in-app to suggest this template
    booking?: string[];           // booking statuses/types e.g. ['confirmed','hotel']
    invoice?: string[];           // invoice statuses e.g. ['due','overdue']
    crm?: string[];               // lead stages
  };
}

const varsFromBody = (body: string): string[] => {
  const set = new Set<string>();
  for (const m of body.matchAll(/\{\{\s*([a-z0-9_]+)\s*\}\}/gi)) set.add(m[1].toLowerCase());
  return Array.from(set);
};

// Helper to reduce boilerplate
const T = (t: Omit<LibraryTemplate, 'variables'>): LibraryTemplate => ({
  ...t,
  variables: varsFromBody(`${t.header ?? ''} ${t.body} ${t.footer ?? ''}`),
});

